-- Employee count by department number
SELECT COUNT(current_emp.emp_no), dept_emp.dept_no
FROM current_emp as current_emp
LEFT JOIN dept_emp
ON current_emp.emp_no = dept_emp.emp_no
GROUP BY dept_emp.dept_no;

-- Employee count by department number
